        <h1>Programa de Acesso e Permanência a Estudantes Imigrantes (Pró-Imigrante)</h1>
        <p>O Programa de Acesso e Permanência a Estudantes Imigrantes (Pró-Imigrante) proporciona acesso ao ensino superior a imigrantes residentes no Brasil. É constituído por um conjunto de serviços, projetos e ações articuladas com as demais políticas institucionais e acadêmicas, e visa ao fortalecimento das condições de acesso, permanência e êxito nas atividades acadêmicas dos estudantes imigrantes da Instituição. Foi instituído pela Resolução nº 16/CONSUNI/UFFS/2019.</p>
        
        <p>Além de propiciar o ingresso no ensino superior, o Pró-Imigrante busca promover o intercâmbio e a integração cultural entre estudantes imigrantes e brasileiros, dentro e fora da Universidade. Conheça mais sobre o Programa clicando <a href="#">aqui</a>.</p>
        
        <p>É fundamental que os candidatos tenham o ensino médio completo e, quando tiverem cursado em seu país de origem, deverá ser providenciada a equivalência de estudos ao ensino médio brasileiro. O processo deve ser realizado junto à Secretaria de Estado da Educação do estado em que o candidato reside.</p>

        <p>O ingresso nos cursos de graduação ocorre de duas formas:</p>
        <ol>
            <li>De acordo com o definido na Resolução nº 08/2016- CONSUNI/CGRAD (Política de Ingresso da UFFS), via Enem/SiSU, alterada pela Resolução nº 08/CONSUNICGAE/UFFS/2016;</li>
            <li>Mediante Processo Seletivo Específico, que ocorre anualmente, e os candidatos selecionados são matriculados como alunos regulares nos cursos de graduação da UFFS, tendo os mesmos direitos e deveres dos demais estudantes a Instituição. Atualmente, são ofertadas vagas em cursos de graduação ofertados pela Universidade nos campi de Cerro Largo, Chapecó, Erechim, Laranjeiras do Sul e Realeza.</li>
        </ol>

        <p>O Pró-Imigrante foi precedido pelo Programa Prohaiti, que era destinado ao ingresso de estudantes haitianos na graduação. Com o Pró-Imigrante, foi ampliado o acesso a estudantes imigrantes de todas as nacionalidades, atendendo ao recente cenário das migrações recentes.</p>

        <p>Atualmente a UFFS possui um número significativo de estudantes imigrantes nos mais diferentes cursos e campi, também já tivemos vários alunos graduados nos últimos anos, sinalizando a importância desses Programas para os estudantes e para a sociedade.</p>
